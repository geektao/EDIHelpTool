package tonyx.EDI.SyntaxTokens;

final public class Range {
	private String ediStart;
	private String ediEnd;

	public String getEdiStart() {
		return ediStart;
	}

	public void setEdiStart(String ediStart) {
		this.ediStart = ediStart;
	}

	public String getEdiEnd() {
		return ediEnd;
	}

	public void setEdiEnd(String ediEnd) {
		this.ediEnd = ediEnd;
	}
}
