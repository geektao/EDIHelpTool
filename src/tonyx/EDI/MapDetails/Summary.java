package tonyx.EDI.MapDetails;

final public class Summary {
	private String ediAuthor;
	private String ediDescription;
	private String ediMapFunction;

	public String getEdiAuthor() {
		return ediAuthor;
	}

	public void setEdiAuthor(String ediAuthor) {
		this.ediAuthor = ediAuthor;
	}

	public String getEdiDescription() {
		return ediDescription;
	}

	public void setEdiDescription(String ediDescription) {
		this.ediDescription = ediDescription;
	}

	public String getEdiMapFunction() {
		return ediMapFunction;
	}

	public void setEdiMapFunction(String ediMapFunction) {
		this.ediMapFunction = ediMapFunction;
	}
}