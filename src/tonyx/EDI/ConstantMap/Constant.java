package tonyx.EDI.ConstantMap;

final public class Constant {
	private String ediConstantID;
	private String ediType;
	private String ediValue;

	public String getEdiConstantID() {
		return ediConstantID;
	}

	public void setEdiConstantID(String ediConstantID) {
		this.ediConstantID = ediConstantID;
	}

	public String getEdiType() {
		return ediType;
	}

	public void setEdiType(String ediType) {
		this.ediType = ediType;
	}

	public String getEdiValue() {
		return ediValue;
	}

	public void setEdiValue(String ediValue) {
		this.ediValue = ediValue;
	}
}
