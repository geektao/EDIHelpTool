package tonyx.EDI.Common;

final public class ExplicitRule {
	private String ediOnBegin;
	private String ediOnEnd;

	public String getEdiOnBegin() {
		return ediOnBegin;
	}

	public void setEdiOnBegin(String ediOnBegin) {
		this.ediOnBegin = ediOnBegin;
	}

	public String getEdiOnEnd() {
		return ediOnEnd;
	}

	public void setEdiOnEnd(String ediOnEnd) {
		this.ediOnEnd = ediOnEnd;
	}
}
